// Usando Math em JavaScript

// Número PI
let new1 = Math.PI;
console.log("Valor de PI:", new1);

// Arredondar números

// Arredonda para o inteiro mais próximo
let new2 = Math.round(3.53); 
console.log("Arredondado (Math.round):", new2); 

// Arredonda sempre para baixo
let new3 = Math.floor(3.99);
console.log("Arredondado para baixo (Math.floor):", new3);

// Arredonda sempre para cima
let new4 = Math.ceil(3.1);
console.log("Arredondado para cima (Math.ceil):", new4);

// Retorna o valor absoluto
let new5 = Math.abs(-9.34543);
console.log("Valor absoluto (Math.abs):", new5);

// Retorna o menor valor entre os argumentos
let new6 = Math.min(7, 100, 500, 3); 
console.log("Menor valor (Math.min):", new6);

// Retorna o maior valor entre os argumentos
let new7 = Math.max(7, 100, 500, 3);
console.log("Maior valor (Math.max):", new7);

// Gera um número aleatório entre 0 e 1
let new8 = Math.random();
console.log("Número aleatório (Math.random):", new8);

// Gera um número aleatório entre 0 e 100
let new9 = Math.floor(Math.random() * 100); 
console.log("Número aleatório entre 0 e 100 arredondado para baixo:", new9);

// Funções trigonométricas
let new10 = Math.sin(1); // Seno
console.log("Seno (Math.sin):", new10);

let new11 = Math.cos(1); // Cosseno 
console.log("Cosseno (Math.cos):", new11);

let new12 = Math.tan(1); // Tangente 
console.log("Tangente (Math.tan):", new12);